package com.example.neptune.data.model.streamingConnector.spotifyConnector


/**
 * Enum class representing the playback states of a Spotify player..
 */
enum class PlaybackState {
    /** Indicates that the player is currently playing a track. */
    PLAYING,

    /** Indicates that the player is currently paused. */
    PAUSED,

    /** Indicates the initial state of the player. */
    INITIAL
}